// What is the difference between System.out.print() and
// System.out.println() ?

public class H1 {

    public static void main(String[] args) {
        System.out.print("Hello World!");
        System.out.println(", Hello World!");
        System.out.println("Hello World!");
    }  // main

}  // H1 class



// A basic introductory Hello World Program
// No programming can be done outside of a class and method within Java
// As seen above, within the H1 class, the main method contains the actuall print statements that print hello world
